#!/usr/bin/env python3
"""
This module provides a function to convert a floating point number to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a floating point number to its string representation.

    Parameters:
    n (float): The floating point number to be converted.

    Returns:
    str: The string representation of the floating point number.
    """
    return str(n)
